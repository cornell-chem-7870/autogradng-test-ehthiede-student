def add_two_numbers(a: float, b: float) -> float:
    """
    Evaluates the sum of two numbers and returns the result.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.

    Example:
        add_two_numbers(1.0, 2.0) -> 3.0

    """
    return a + b
